Dalam upaya peningkatan mutu pendidikan Politeknik di Indonesia, Direktorat Jenderal Pendidikan Tinggi Kementerian Pendidikan dan Kebudayaan memberikan dana pengembangan mutu kepada 13 Politeknik Negeri melalui program penugasan dan kepada Politeknik Negeri (selain 13 politeknik yang mendapat penugasan) maupun Politeknik Swasta melalui kompetisi yaitu dengan Program Hibah Kompetisi Pengembangan Mutu Pendidikan Politeknik (PHK-PMPP).<br />
<br />
Program pengembangan politeknik ini searah dengan tujuan dari Master Plan Percepatan dan Perluasan Pembangunan Ekonomi Indonesia (MP3EI) tahun 2011 â€“ 2025 untuk 5 (lima) sektor, yaitu Infrastruktur, Agroindustri, Pariwisata, Manufaktur dan Pertambangan, dan lebih menekankan pada pengembangan internal, maka dalam program pengembangan politeknik ini tidak hanya peningkatan kapasitas internal yang menjadi tujuannya tetapi juga peningkatan relevansi yang sesuai
dengan kebutuhan industri serta pengembangan kerja sama dengan industri yang searah dengan Masterplan Percepatan
dan Perluasan Pembangunan Ekonomi Indonesia (MP3EI).<br />
<br />
Ini adalah panduan mengenai pengisian data Program Plan<br />
<br />
